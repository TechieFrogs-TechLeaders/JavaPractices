public class SwitchCaseDemo {
 public static void main(String[] args)  {
    int weekday=3;
    switch(weekday) {
        case 1:
        System.out.println("sunday");
       break;
       case 2:
       System.out.println("monday");
       break;
       case 3:
       System.out.println("Tuesday");
       break;
    }
 }
}

