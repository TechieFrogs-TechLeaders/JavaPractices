public class switchcondition{
    public static void main(String args[]){
        int a=10,b=20;
        switch(a+b){
            case 10:
            System.out.println("variable is b");
            break;
            case 20:
            System.out.println("variable is c");
            break;
            case 40:
            System.out.println("variable is a");
        default:
        System.out.println("nothing matched");


        }

    }
    
    
}
