public class AssignOp{
    public static void main(String args[]){

        int a=25,b=35,c=45;
        float f1=26.05f,f2=27.62f,f3=28.53f;
        boolean b1=true,b2=false;
        byte m1=4,m2=6;
        char c1='A',c2='4',c3='@';//it accepts both constants and variables
        short s1=-32767,s2=327;
        long l1=-23456L,l2=234566789L;
        double d1=23.23d,d2=29.29d;
        //assignment operators for integer dataype
        a+=10;
        b-=15;
        c+=25;
        //assignment operators for float datatype
        f1+=25.02f;
        f2-=10.06f;
        f3+=30.07f;
        // It doesnot accepts boolean values
        //b1=true;
        //assignment operators for byte datatype
        m1+=10;
        m2-=8;//it accepts negative values
        c1+=2;//assignment operators accepts characters
        c2-=4;
        c3+=5;
        //assignment operators for short datatype
        s1+=2;
        s2-=23;
        //assignment operators for long datatype
        l1+='A';//convert A value in to ASCII value//ASCII Value for A=65 
        l2+=29;
        d1+=28.28;
        d2+=26;


        System.out.println("a= " + a);
        System.out.println("b= " + b);
        System.out.println("c= " + c);
        //Assignment operators accepts float values
        System.out.println("f1= " +f1);
        System.out.println("f2= " +f2);
        System.out.println("f3= " +f3);
        System.out.println("b1= " +b1);
        System.out.println("b2= " +b2);
        System.out.println("m1= " +m1);
        System.out.println("m2= " +m2);
        System.out.println("c1= " +c1);
        System.out.println("c2= " +c2);
        System.out.println("c1= " +c3);
        System.out.println("s1= " +s1);
        System.out.println("s2= " +s2);
        System.out.println("l1= " +l1);
        System.out.println("d1= " +d1);
        System.out.println("d2= " +d2); 











        
        
        
    }
    
}
