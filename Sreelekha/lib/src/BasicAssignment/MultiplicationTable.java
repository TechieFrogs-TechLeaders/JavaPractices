package BasicAssignment;

public class MultiplicationTable {
    public static void main(String[] args){
        int a= 10;
        int b= 3;
        int mul = 0;
        for(int i=1;i<=a;i++){
            mul=b*i;
            System.out.println("for each iteration mul value : " +a+" "+"*"+i+" "+" =" +mul);
        }
    }
}

    




































       