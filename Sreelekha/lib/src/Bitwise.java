public class Bitwise {
    public static void main(String[] args) {
        int a=15, b=20;
        //bitwise operators
        System.out.println(a>b & a<b);
        System.out.println(a>b | a<b);
        //logical operators
        System.out.println(a>b && a<b);
        System.out.println(a>b || a<b);

    }
    
}