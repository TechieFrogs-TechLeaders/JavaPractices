package Classpractice;
public class WhileLoop {
    public static void main(String[] args) {
        int i=15;
        while(i<=35){
            System.out.println(i);
           i++;
        }

        int j=10;
        do{
            System.out.println(j);
            j++;
        }while(j>15);
      
    }
}
