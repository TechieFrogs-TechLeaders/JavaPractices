package BasicAssignment;

public class Swapping {
    public static void main(String[] args) {
        int n1=44,n2=51,temp;
        System.out.println("before swapping n1:"+n1 +" and n2:"+ n2);
        temp=n1;
        n1=n2;
        n2=temp;
        System.out.println("after swapping n1:"+n1 +" and n2:"+ n2);

    }
}
