//4.Java Program to Check Whether a Number is Even or Odd
public class Even_Odd {
    public static void main(String[] args) {
        int a=67;
        if(a%2==0){
            System.out.println("The Number is Even :"+a);
        }
        else{
            System.out.println("The number is Odd:"+a);
        }

        
    }
    
}
