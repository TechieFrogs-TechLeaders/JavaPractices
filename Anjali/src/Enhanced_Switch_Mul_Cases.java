public class Enhanced_Switch_Mul_Cases {
    public static void main(String[] args) {
        int i=10;
        switch(i+100){
            case 3,20,10:
            System.out.println("The value is 10");
            break;
            case 60,50,30:
            System.out.println("The value is 30");
            break;
            case 70:
            System.out.println("The value is 70");
            break;
            default:
            System.out.println("No matching values");


        }
    }
    
}
