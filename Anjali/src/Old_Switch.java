public class Old_Switch {
    public static void main(String[] args) {
        int i=50;
        switch(i){
            case 1:
            System.out.println("The value is 1");
            break;
            case 30:
            System.out.println("The value is 30");
            break;
            case 50:
            System.out.println("The value is 50");
           break;
            default:
            System.out.println("Doesn't match any value");
        }
        
    }
    
}
