public class Shift {
    public static void main(String[] args){
        //Left Shift Operator
    System.out.println(10<<2); //num1*2^num2   40
    System.out.println(15<<2);                //60
    //Right Shift Operator
    System.out.println(18>>3);//num1/2^num2    2
    System.out.println(10>>5);                //0

    }
    
}
