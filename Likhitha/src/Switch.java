public class Switch {
     public static void main(String[] args) {
         int n=10;
         switch (n) {
         case 5:
              System.out.println("=5");
              break;
         case 10:
              System.out.println("=10");
              break;
         case 15:
               System.out.println("=15");
               break;
       }
    }
}
