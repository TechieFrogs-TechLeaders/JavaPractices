public class Switcharrow {
    public static void main(String[] args) {
        short num=5;
        switch (num) {
     case 1 ->
             System.out.println("Less than 10*2");
      case 2 ->
              System.out.println("Greater than 10/2");
      default ->
               System.out.println("Equal or not equal");
      }
    }
}
