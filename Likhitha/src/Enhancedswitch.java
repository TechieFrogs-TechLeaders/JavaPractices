public class Enhancedswitch {
    public static void main(String[] args) {
        int k=20;
        switch (k) {
    case 12,19,39:
           System.out.println("Near to k");
    case 15:
           System.out.println("more than k");
   default:
         System.out.println("No number matched");
        }
    }
}
