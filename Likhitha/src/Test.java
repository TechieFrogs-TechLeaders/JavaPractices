public class Test {
    public static void main(String[] args) {
    int a=12,b=15,c=18;
    //unary operators
    //Increament - prefix;
    a=a+1;
    System.out.println(++a);
    //Decreament- prefix;
    a=a-1;
    System.out.println(--a);
    //Increament - postfix;
    a=a+1;
    System.out.println(a++);
    //Decreament - postfix;
    a=a-1;
    System.out.println(a--);
    System.out.println(a++ + ++b - ++c);
    System.out.println(a+ " " +b + " " +c);
    }
}
    



    

